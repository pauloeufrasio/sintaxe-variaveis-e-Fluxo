public class Main {
    public static void main(String[] args) {

        // Declarado variaveis
        int idade;

        // Atribuindo valor para variavel
        idade = 37;

        // Testando variavel com valor atribuido
        System.out.println(idade);

        // Atribuido um novo valor para variavel
        idade = 30 + 10;
        // Testando variavel com novo valor atribuido
        System.out.println(idade);

        // Atribuido um novo valor para variavel
        idade = (7 * 5) + 2;
        // Testando variavel com novo valor atribuido
        System.out.println(idade);
    }
}